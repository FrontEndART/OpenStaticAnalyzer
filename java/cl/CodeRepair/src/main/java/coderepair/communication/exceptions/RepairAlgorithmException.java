package coderepair.communication.exceptions;


public class RepairAlgorithmException extends Exception {

    private static final long serialVersionUID = -8977656924109390522L;

    public RepairAlgorithmException() {
        super();
    }

    public RepairAlgorithmException(String message, Throwable cause) {
        super(message, cause);
    }

    public RepairAlgorithmException(String message) {
        super(message);
    }

    public RepairAlgorithmException(Throwable cause) {
        super(cause);
    }
    
    
}
